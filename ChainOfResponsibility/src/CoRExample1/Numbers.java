
package CoRExample1;


public class Numbers {
    
    private int Num1;
    private int Num2;
    private String calculation;

    public Numbers(int Num1, int Num2, String calculation) {
        this.Num1 = Num1;
        this.Num2 = Num2;
        this.calculation = calculation;
    }

    public int getNum1() {
        return Num1;
    }

    public int getNum2() {
        return Num2;
    }

    public String getCalculation() {
        return calculation;
    }
    
    
}

